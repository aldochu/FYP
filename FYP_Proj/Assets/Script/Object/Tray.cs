
public class Tray
{
    public int tray;
    public int utensil1;
    public int utensil2;

    public Tray(int tray, int utensil1, int utensil2)
    {
        this.tray = tray;
        this.utensil1 = utensil1;
        this.utensil2 = utensil2;
    }

    public Tray()
    {
        tray = utensil1 = utensil2 = 0;
    }
}
